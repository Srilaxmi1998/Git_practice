package javaExmples;

public class TypeCasting {

	public static void main(String[] args) {
		int a=12;
		byte b=34;
		System.out.println(a=b);
		b=(byte) a;
		System.out.println(b);
		char c='s';
		int i=(char) c;
		System.out.println(i);
		int q=908;
		char w=(char)q;
		System.out.println(w);

	}

}
